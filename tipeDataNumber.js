/*
        =====TIPE DATA NUMBER=====
        - js hanya mendukung satu tipe data number, dimana tipe data number di js bisa berupa bilangan bulat atau bilangan decimal
       - tipe seperti di bahasa pemrograman lain yang biasanya membedakan tipe data number bilangan bulat dan bilangan desimal, di js semua di satukan
    */
console.log(100);
console.log(100.1); //=> jadi 100 koma 100 (koma = .)

/*
        ======NUMBER NOTATION======
        - js mendukung number notation, defaultnya ada basis 10, js juga mendukung binary, hexadecimal dan octal
       - hexadecimal : 0xFF
       - binary : 0b10101
       - Octal : Oo10
    */
console.log(0b1111010);
