/*
        ======TIPE DATA STRING======
       - tipe data string atau text adalah tipe data yang berisikan kumpulan kosongan atau lebih karakter
       - sebelumnya kita sudah pernah menggunakan string ketika membuat program hello world
       - di js, untuk membuat data dengan tipe string, kita perlu menggunaan "petik dua" atau 'petik satu' sebelum dan setelah textnya.
       */
console.log("aulia martha brielliant");
console.log("kelas 12 rpl 1");

/*
        =====MENAMBAH STRING=====
       - data string bisa digabung menggunakan operator + (plush)
       */
console.log("aulia martha brielliant : " + "slebew");
console.log("kelas 12 rpl 1 : " + "kelas terbaik di alam semesta");
