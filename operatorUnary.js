/*
       ======OPERATOR UNARY======
       | +  | menandakan positif            |
       | -  | menandakan negatif            |
       | ++ | increment, menaikan 1 angka   |
       | -- | decrement, menurunkan 1 angka |
       */
let result = +1;
console.log(result); //=> result = positif 1

result -= 1;
console.log(result); //=> 1 - 1= 0

result--;
console.log(result); //=> 0 - 1 = -1

result++;
console.log(result); //=> -1 + 1 = 0
